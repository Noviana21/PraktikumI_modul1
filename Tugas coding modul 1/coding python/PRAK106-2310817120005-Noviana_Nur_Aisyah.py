a = 4
b = 8
c = 3

# boolean
pintu = a == b
kulkas = b > c
mesin_cuci = a != c

print(f'Variabel a bernilai {a}')
print(f'Variabel b bernilai {b}')
print(f'Variabel c bernilai {c}')

print(f'Apakah a sama dengan b ? jawabannya adalah {int(pintu)}')
print(f'Apakah b lebih besar dari c ? jawabannya adalah {int(kulkas)}')
print(f'Apakah a tidak sama dengan c ? jawabannya adalah {int(mesin_cuci)}')